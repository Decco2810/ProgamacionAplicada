# 2 to the 10th power, or 1024
print("2 elebado a la 10: ")
print(2 ** 10)
 
# 8 squared, or 64
print("8 elebado a la 2: ")
print(8 ** 2)
 
# 9 * 9 * 9, 9 cubed, or 729
print("9 elebado a la 3: ")
print(9 ** 3)
 
# We can even perform fractional exponents
# 4 to the half power, or 2
print("4 elebado a la 0.5: ")
print(4 ** 0.5)